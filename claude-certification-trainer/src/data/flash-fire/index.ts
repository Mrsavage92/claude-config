import type { FlashFire } from '@/schemas';

/**
 * Flash Fire items: short, fast-recognition drills (10–20s each).
 *
 * All items are independently authored for this trainer, grounded in the
 * concepts in the adapted CCAO-F guide. They are never presented as official.
 * Each links back to a full lesson and/or full question for deeper study.
 */

function ff(
  id: string,
  domainId: string,
  prompt: string,
  options: string[],
  correct: string[],
  explanation: string,
  extra: Partial<FlashFire> = {},
): FlashFire {
  return {
    id,
    certificationId: 'ccao-f',
    domainId,
    prompt,
    answerOptions: options.map((text, i) => ({ id: String.fromCharCode(97 + i), text })),
    correctAnswerIds: correct,
    explanation,
    relatedQuestionIds: [],
    relatedLessonIds: [],
    sourceIds: [],
    provenance: 'independently-authored',
    estimatedTimeSeconds: 15,
    enabled: true,
    ...extra,
  };
}

